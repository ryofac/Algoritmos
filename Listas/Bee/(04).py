def calcular_media(num1, num2, num3):
    return (num1 * 2 + num2 * 3 + num3 * 5) / 10
    
def main():
    # Entrada
    a = float(input())
    b = float(input())
    c = float(input())
    # Processamento
    media = calcular_media(a,b,c)
    # Saida
    print(f'MEDIA = {media}')

main()


