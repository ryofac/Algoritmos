# Entrada
num1 = int(input('Digite um número: '))
num2= int(input('Digite outro número: '))
num3 = int(input('Digite mais um número: '))

# Processamento
diff_primeiros = num1 - num2
soma_ultimos = num2 + num3

# Saída
print(f'A diferença entre os dois primeiros é de {diff_primeiros} e entre os dois últimos é de {soma_ultimos}')